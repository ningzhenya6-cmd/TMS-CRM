db = sqlite3.connect(DB)
db.row_factory = sqlite3.Row

count = 0
for entry in entries:
    eid = f"kb-foundation-{uuid.uuid4().hex[:8]}"
    now = time.strftime("%Y-%m-%d %H:%M")
    existing = db.execute("SELECT id FROM entries WHERE title=?", (entry['title'],)).fetchone()
    if existing:
        continue
    db.execute(
        "INSERT INTO entries(id,title,content,category,tags,related_questions,is_active,created_at,updated_at) VALUES(?,?,?,?,?,?,1,?,?)",
        (eid,entry['title'],entry['content'],entry['category'],json.dumps(entry.get('tags',[])),json.dumps([]),now,now))
    count += 1
    print(f"  ✅ {entry['title'][:45]}")

db.commit()
total = db.execute("SELECT COUNT(*) FROM entries").fetchone()[0]
db.close()
